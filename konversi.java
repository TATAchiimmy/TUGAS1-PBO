package tugas1pbo;

public class konversi{
    public static double kelvin() {
        return (main.celcius + 273.15);
    }
    public static double reamur(){
        return ((main.celcius*4)/5);
    }
    public static double farenheit(){
        return ((main.celcius*9)/5+32);
    }
}


